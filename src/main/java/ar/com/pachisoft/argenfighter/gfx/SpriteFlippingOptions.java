package ar.com.pachisoft.argenfighter.gfx;

/**
 * Indicates how an sprite should be rotated
 */
public enum SpriteFlippingOptions {
    /**
     * Do not flip the sprite
     */
    FLIP_NONE,
    /**
     * Flip the sprite horizontally
     */
    FLIP_HORIZONTALLY,
    /**
     * Flip the sprite vertically
     */
    FLIP_VERTICALLY,
    /**
     * Flip the sprite both horizontally and vertically (similar to flip 180 deg.)
     */
    FLIP_BOTH
}
